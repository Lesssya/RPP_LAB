# Считываем строку с числами
input_str = input("Введите числа через пробел: ")

# Инициализируем переменные
total_sum = 0
count = 0
index = 0
current_num_str = ""

# Обрабатываем строку с помощью while
while index < len(input_str):
    char = input_str[index]

    # Если символ - цифра или минус, добавляем к текущему числу
    if char.isdigit() or (char == '-' and not current_num_str):
        current_num_str += char
    # Если пробел и есть накопленное число, обрабатываем его
    elif char == ' ' and current_num_str:
        num = int(current_num_str)
        total_sum += num
        count += 1
        current_num_str = ""

    index += 1

# Обрабатываем последнее число в строке (если есть)
if current_num_str:
    num = int(current_num_str)
    total_sum += num
    count += 1

# Выводим результаты
print(f"Сумма чисел: {total_sum}")
print(f"Количество чисел: {count}")